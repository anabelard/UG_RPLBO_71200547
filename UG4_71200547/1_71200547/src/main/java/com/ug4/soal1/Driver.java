package com.ug4.soal1;

public class Driver {
    String name;
    int age;
    String gender;

    public String getName(){
        return name;
    }
    public void setName(String namaDriver){
        name = namaDriver;
    }
    public int getAge(){
        return age;
    }
    public void setAge(int umur){
        age = umur;
    }
    public String getGender(){
        return gender;
    }
    public void setGender(String x){
        gender = x;
    }

}
